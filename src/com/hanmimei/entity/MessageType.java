package com.hanmimei.entity;

public class MessageType {
	private int sysNum;
	private int goodNum;
	private int wuliuNum;
	private int zichanNum;
	private int huodongNum;
	private int code;
	private String message;
	public int getSysNum() {
		return sysNum;
	}



	public void setSysNum(int sysNum) {
		this.sysNum = sysNum;
	}



	public int getGoodNum() {
		return goodNum;
	}



	public void setGoodNum(int goodNum) {
		this.goodNum = goodNum;
	}



	public int getWuliuNum() {
		return wuliuNum;
	}



	public void setWuliuNum(int wuliuNum) {
		this.wuliuNum = wuliuNum;
	}



	public int getZichanNum() {
		return zichanNum;
	}



	public void setZichanNum(int zichanNum) {
		this.zichanNum = zichanNum;
	}



	public int getHuodongNum() {
		return huodongNum;
	}



	public void setHuodongNum(int huodongNum) {
		this.huodongNum = huodongNum;
	}



	public int getCode() {
		return code;
	}



	public void setCode(int code) {
		this.code = code;
	}



	public String getMessage() {
		return message;
	}



	public void setMessage(String message) {
		this.message = message;
	}



	public MessageType() {
		super();
	}

}
